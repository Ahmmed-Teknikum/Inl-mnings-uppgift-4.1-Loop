    class Program
    {
        static void Main(string[] args)
        {
            for(int x=10;x<31;x++)
            {
               Console.WriteLine(x);
            }
            {
                for(int y=200;y>179;y--)
                {
                    Console.WriteLine(y);
                }
                {
                    for(int z=1000;z<1401;z=z+50)
                    {
                        Console.WriteLine(z);
                    }
                }
            }
        }
    }
}